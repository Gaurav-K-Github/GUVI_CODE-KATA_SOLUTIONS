# Write a program to check whether a given number is even or odd. If even display “Even” else display “oDd”

# Input Description:
# First line consists of input number

# Output Description:
# Output consists of “Even” or “oDd”

# Sample Input :
# 2
# Sample Output :
# Even

#==============================================================================================

# -----------
# Source code:
# -----------

i=int(input())

if i%2==0:
    print("Even")
else:
    print("oDd")
