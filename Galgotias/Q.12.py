# Write a program to calculate the sum of all numbers from 1 to a given number. If the input is zero print “0”

# Input Description:
# First line consists of input number

# Output Description:
# Output consists sum of all numbers

# Sample Input :
# 2
# Sample Output :
# 3

#==============================================================================================

# -----------
# Source code:
# -----------

i=int(input())
s=0
for i in range(0,i+1):
    s+=i
print(s)    
