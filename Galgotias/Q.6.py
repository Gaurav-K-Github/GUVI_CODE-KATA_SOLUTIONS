# Write a program to check whether a number is divisible by 11 or not. If divisible display “yes” else display “no”

# Input Description:
# First line consists of input number

# Output Description:
# Output consists of “yes” or “no”

# Sample Input :
# 22
# Sample Output :
# yes

#==============================================================================================

# -----------
# Source code:
# -----------

i=int(input())

if i%11==0:
    print('yes')
else:
    print('no')
